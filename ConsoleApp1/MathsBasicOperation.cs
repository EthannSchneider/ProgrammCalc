using System;

namespace Math
{
    public class MathsBasicOperation
    {
        public static int Add(int op1, int op2)
        {
            return op1 + op2;
        }
        public static int Substract(int op1, int op2)
        {
            return op1 - op2;
        }
        public static int Multiply(int op1, int op2)
        {
            return op1 * op2;
        }
        public static int Divide(int op1, int op2)
        {
            return op1 / op2;
        }
    }
}

